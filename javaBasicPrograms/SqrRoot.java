package javaBasicPrograms;

public class SqrRoot {

	public static void main(String[] args) {
		int number =626;
		double sqrRoot=Math.sqrt(number);
		System.out.println("Square Root of a number "+number+" is "+sqrRoot);
		int n1=3;
		double r=Math.cbrt(n1);
		System.out.println("Cube Root of a number is "+r);
		double r1=Math.pow(n1, 2);
		System.out.println("root of a number is "+r1);
	}
}
